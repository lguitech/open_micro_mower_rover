int counter = 5;
int flag = 0;
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
  if (htim->Instance == TIM3)
  {
    counter --;
    if (counter > 0) {
      return;
    }
    counter = 5;
    
    flag = !flag;
    if (flag) {
      led2_on();
    }
    else {
      led2_off();
    }
  }
}

